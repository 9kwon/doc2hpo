package edu.columbia.dbmi.doc2hpo.pojo;

public class Version {
	
	public String doc2hpo;
	public String java;
	public String tomcat;
	public String metamap;
	public String ncbo;
	
	public String getDoc2hpo() {
		return doc2hpo;
	}
	public void setDoc2hpo(String doc2hpo) {
		this.doc2hpo = doc2hpo;
	}
	public String getJava() {
		return java;
	}
	public void setJava(String java) {
		this.java = java;
	}
	public String getTomcat() {
		return tomcat;
	}
	public void setTomcat(String tomcat) {
		this.tomcat = tomcat;
	}
	public String getMetamap() {
		return metamap;
	}
	public void setMetamap(String metamap) {
		this.metamap = metamap;
	}
	public String getNcbo() {
		return ncbo;
	}
	public void setNcbo(String ncbo) {
		this.ncbo = ncbo;
	}

}
